/**
 * Definition for singly-linked list.
 * public class ListNode {
 * int val;
 * ListNode next;
 * ListNode() {}
 * ListNode(int val) { this.val = val; }
 * ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    public ListNode reverseList(ListNode head) {
ListNode front=null;
while (head!=null){
    ListNode back = head.next;    //123        head=1 back=2 front=null   back=3           b=null
    head.next = front;            //head=1 head.next=null                  2.next=1         3.2
    front=head;                     //front=1                              front=2          3.2.1
    head=back;                    //  head=2                               head=3        null
}return front;
    }
}

